    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
     
        int nivelTemp;
        if(txtNivelAcceso.getSelectedItem().toString().equals("Normal")){
            nivelTemp=1;
        }
        else{
            nivelTemp=0;
        }
        if (txtUsuario.getText().trim().isEmpty() || txtContraseña.getText().trim().isEmpty() || txtCorreo.getText().trim().isEmpty() || txtApellido.getText().trim().isEmpty() || txtNombre.getText().trim().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Complete todos los campos");
    return;
}
        
        
        try {
        File archivo = getArchivoUsuarios();

        Scanner leer = new Scanner(archivo);
        String usuarioIngresado = txtUsuario.getText().trim();
 //aqui para identificar si existe un usuario con el mismo nombre,
        while (leer.hasNextLine()) {
            String linea = leer.nextLine();
            String[] d = linea.split(";");
            if (d[0].equalsIgnoreCase(usuarioIngresado)) {
                leer.close();
                JOptionPane.showMessageDialog(this, "El nombre de usuario ya existe, ingrese otro.");
                return;
            }
        }
        leer.close();

        FileWriter fw = new FileWriter(archivo, true);

        
        nivel();
        fw.write(
            txtUsuario.getText() + ";" +
            txtContraseña.getText() + ";" +
            nivelTemp + ";" +
            txtNombre.getText() + ";" +
            txtApellido.getText() + ";" +
            txtCorreo.getText() + "\n"
        );
       

        fw.close();
        JOptionPane.showMessageDialog(this, "Usuario guardado correctamente.");
        limpiarCampos();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al guardar: " + e.getMessage());
    }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
    String usuarioBuscado = txtUsuario.getText();

    File archivo = new File("Usuarios.txt");
    File archivoTemp = new File("Usuarios_temp.txt");

    try {
        BufferedReader br = new BufferedReader(new FileReader(archivo));
        BufferedWriter bw = new BufferedWriter(new FileWriter(archivoTemp));

        String linea;
        boolean encontrado = false;

        while ((linea = br.readLine()) != null) {

            String[] datos = linea.split(";");
nivel();
           
            if (datos[0].equals(usuarioBuscado)) {
                String nuevaLinea = txtUsuario.getText() + ";" +
                                    txtContraseña.getText() + ";" +
                                    nivelTemp + ";" +
                                    txtNombre.getText() + ";" +
                                    txtApellido.getText() + ";" +
                                    txtCorreo.getText();

                bw.write(nuevaLinea);
                encontrado = true;

            } else {
                // dejamos la línea igual
                bw.write(linea);
            }

            bw.newLine();
        }

        br.close();
        bw.close();

       // para reemplazar archivo viejo por el nuevo  y renombrar el nuevo a archivo
        archivo.delete();
        archivoTemp.renameTo(archivo);

        if (encontrado) {
            JOptionPane.showMessageDialog(this, "Usuario modificado.");
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(this, "Usuario no encontrado.");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       try {
        File archivo = getArchivoUsuarios();
        File temp = new File("TempUsuarios.txt");

        Scanner sc = new Scanner(archivo);
        BufferedWriter bw = new BufferedWriter(new FileWriter(temp));

        boolean eliminado = false;

        while (sc.hasNextLine()) {
            String linea = sc.nextLine();
            String[] d = linea.split(";");

            if (d[0].equals(txtUsuario.getText())) {
                eliminado = true;
                continue; 
            }

            bw.write(linea);
            bw.newLine();
        }

        sc.close();
        bw.close();

        archivo.delete();
        temp.renameTo(archivo);

        if (eliminado) {
            JOptionPane.showMessageDialog(this, "Usuario eliminado.");
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(this, "Usuario no encontrado.");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al eliminar ");
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

