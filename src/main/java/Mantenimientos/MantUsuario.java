    private void txtUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtUsuarioActionPerformed
      txtNombre.requestFocus();
      String usuarioBuscado = txtUsuario.getText().trim();
    if (usuarioBuscado.isEmpty()) return;

    File archivo = new File("Usuarios.txt");

 
    if (!archivo.exists()) {
        return;
    }

    try (BufferedReader br = new BufferedReader(new FileReader(archivo))) {
        String linea;
        boolean encontrado = false;

        while ((linea = br.readLine()) != null) {
            String[] datos = linea.split(";");

            if (datos.length == 6 && datos[0].equals(usuarioBuscado)) {
                txtContraseña.setText(datos[1]);
                txtNivelAcceso.setSelectedItem(datos[2]);
                txtNombre.setText(datos[3]);
                txtApellido.setText(datos[4]);
                txtCorreo.setText(datos[5]);

                encontrado = true;
                break;
            }
        }

      
        if (!encontrado) {
            txtContraseña.setText("");
            txtNivelAcceso.setSelectedIndex(0);
            txtNombre.setText("");
            txtApellido.setText("");
            txtCorreo.setText("");
        }

    } catch (Exception e) {
     
        
    }
      
      
    }//GEN-LAST:event_txtUsuarioActionPerformed

