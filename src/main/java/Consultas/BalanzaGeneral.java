    private void btnMostrarTodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarTodoActionPerformed

       DefaultTableModel modelo = (DefaultTableModel) tblBalanza.getModel();
modelo.setRowCount(0);

try (BufferedReader br = new BufferedReader(new FileReader("CatalogoCuenta.txt"))) {
    String linea;
    while ((linea = br.readLine()) != null) {
        String[] datos = linea.split("\\|");

      
        String codigo = datos[0];
        String nombre = datos[1];
        String tipo = datos[5]; 
        String saldoDeudor = datos[8]; 
        String saldoAcreedor = datos[9]; 
        String balance = datos[10]; 

        modelo.addRow(new Object[]{codigo, nombre, tipo, saldoDeudor, saldoAcreedor, balance});
    }
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error al cargar la Balanza General");
}
    }//GEN-LAST:event_btnMostrarTodoActionPerformed

